#1. Exchange Integers

#Read two integer numbers and, after that, exchange their values. Print the variable values before and after the exchange, as shown below:

first_number = int(input())
second_number = int(input())
print(f'Before:')
print(f'a = {first_number}')
print(f'b = {second_number}')
print(f'After:')
print(f'a = {second_number}')
print(f'b = {first_number}')
